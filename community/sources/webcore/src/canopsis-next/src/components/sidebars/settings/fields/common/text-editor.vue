<template lang="pug">
  settings-button-field(
    :isEmpty="isValueEmpty",
    @create="openTextEditorModal",
    @edit="openTextEditorModal",
    @delete="deleteMoreInfoTemplate"
  )
    template(#title="")
      div.subheading {{ title }}
</template>

<script>
import { MODALS } from '@/constants';

import { formMixin } from '@/mixins/form';

import SettingsButtonField from '@/components/sidebars/settings/fields/partials/button-field.vue';

export default {
  components: { SettingsButtonField },
  mixins: [formMixin],
  props: {
    value: {
      type: String,
      default: '',
    },
    title: {
      type: String,
      default: '',
    },
  },
  computed: {
    isValueEmpty() {
      return !this.value || !this.value.length;
    },
  },
  methods: {
    openTextEditorModal() {
      this.$modals.show({
        name: MODALS.textEditor,
        config: {
          text: this.value,
          action: value => this.updateModel(value),
        },
      });
    },

    deleteMoreInfoTemplate() {
      this.$modals.show({
        name: MODALS.confirmation,
        config: {
          action: () => this.updateModel(''),
        },
      });
    },
  },
};
</script>
