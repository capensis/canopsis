<template lang="pug">
  v-form(v-click-outside.zIndex="showConfirmationModal", @submit.prevent.stop="submit")
    v-card(width="400")
      v-card-title.primary.pa-2.white--text
        v-layout(justify-space-between, align-center)
          h4 {{ title }}
          v-btn.ma-0.ml-3(icon, small, @click="close")
            v-icon(color="white") close
      v-card-text
        point-form(v-model="form", :coordinates="coordinates")
      v-layout(justify-end)
        v-btn(
          :disabled="submitting",
          depressed,
          flat,
          @click="close"
        ) {{ $t('common.cancel') }}
        v-btn.error(
          v-if="editing",
          :disabled="submitting",
          depressed,
          flat,
          @click.stop="$emit('remove')"
        ) {{ $t('common.delete') }}
        v-btn.primary(
          :disabled="isDisabled",
          :loading="submitting",
          type="submit"
        ) {{ $t('common.submit') }}
</template>

<script>
import { MODALS } from '@/constants';

import { submittableMixinCreator } from '@/mixins/submittable';

import PointForm from '@/components/other/map/partials/point-form.vue';

export default {
  $_veeValidate: {
    validator: 'new',
  },
  components: { PointForm },
  mixins: [
    submittableMixinCreator(),
  ],
  props: {
    point: {
      type: Object,
      required: true,
    },
    editing: {
      type: Boolean,
      required: false,
    },
    coordinates: {
      type: Boolean,
      required: false,
    },
  },
  data() {
    return {
      form: { ...this.point },
    };
  },
  computed: {
    title() {
      if (this.editing) {
        return this.$t('map.editPoint');
      }

      return this.$t('map.addPoint');
    },
  },
  watch: {
    point(point) {
      this.form = { ...point };
    },
  },
  methods: {
    close() {
      this.$emit('cancel');
    },

    async submit() {
      const isFormValid = await this.$validator.validateAll();

      if (isFormValid) {
        this.$emit('submit', this.form);
      }
    },

    showConfirmationModal() {
      this.$modals.show({
        id: this.point._id,
        name: MODALS.clickOutsideConfirmation,
        dialogProps: {
          persistent: true,
        },
        config: {
          action: (confirmed) => {
            if (confirmed) {
              return this.submit();
            }

            return this.close();
          },
        },
      });
    },
  },
};
</script>
