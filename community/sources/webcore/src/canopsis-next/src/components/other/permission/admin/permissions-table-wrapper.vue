<template lang="pug">
  div.pa-3.permissions-table-wrapper
    permissions-groups-table(
      v-if="isGroup",
      :groups="permissions",
      :roles="roles",
      :changed-roles="changedRoles",
      :disabled="disabled",
      @change="$listeners.change"
    )
    permissions-table(
      v-else,
      :permissions="permissions",
      :roles="roles",
      :changedRoles="changedRoles",
      :disabled="disabled",
      @change="$listeners.change"
    )
</template>

<script>
import PermissionsTable from './permissions-table.vue';
import PermissionsGroupsTable from './permissions-groups-table.vue';

export default {
  components: {
    PermissionsTable,
    PermissionsGroupsTable,
  },
  props: {
    permissions: {
      type: Array,
      required: true,
    },
    roles: {
      type: Array,
      required: true,
    },
    changedRoles: {
      type: Object,
      required: true,
    },
    disabled: {
      type: Boolean,
      default: false,
    },
  },
  computed: {
    isGroup() {
      return this.permissions.length && this.permissions[0].permissions;
    },
  },
};
</script>

