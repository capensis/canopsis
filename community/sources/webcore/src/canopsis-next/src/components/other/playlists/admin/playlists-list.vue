<template lang="pug">
  c-advanced-data-table(
    :headers="headers",
    :items="playlists",
    :loading="pending",
    :total-items="totalItems",
    :pagination="pagination",
    advanced-pagination,
    select-all,
    expand,
    search,
    @update:pagination="$emit('update:pagination', $event)"
  )
    template(slot="fullscreen", slot-scope="props")
      c-enabled(:value="props.item.fullscreen")
    template(slot="interval", slot-scope="props") {{ props.item.interval | duration }}
    template(slot="enabled", slot-scope="props")
      c-enabled(:value="props.item.enabled")
    template(slot="actions", slot-scope="props")
      v-layout(row)
        c-action-btn(:tooltip="$t('common.play')")
          v-btn.mx-1(
            slot="button",
            :to="getPlaylistRouteById(props.item._id, true)",
            icon
          )
            v-icon play_arrow
        c-copy-btn(
          :value="getPlaylistRouteFullUrlById(props.item._id)",
          :tooltip="$t('common.copyLink')",
          @success="onSuccessCopied",
          @error="onErrorCopied"
        )
        c-action-btn(
          v-if="hasCreateAnyPlaylistAccess",
          type="duplicate",
          @click="$emit('duplicate', props.item)"
        )
        c-action-btn(
          v-if="hasUpdateAnyPlaylistAccess",
          type="edit",
          @click="$emit('edit', props.item)"
        )
        c-action-btn(
          v-if="hasDeleteAnyPlaylistAccess",
          type="delete",
          @click="$emit('remove', props.item._id)"
        )
    template(slot="expand", slot-scope="props")
      playlist-list-expand-item(:playlist="props.item")
</template>

<script>
import { APP_HOST } from '@/config';

import { removeTrailingSlashes } from '@/helpers/url';

import { permissionsTechnicalPlaylistMixin } from '@/mixins/permissions/technical/playlist';

import PlaylistListExpandItem from './playlists-list-expand-item.vue';

export default {
  components: {
    PlaylistListExpandItem,
  },
  mixins: [permissionsTechnicalPlaylistMixin],
  props: {
    playlists: {
      type: Array,
      required: true,
    },
    pending: {
      type: Boolean,
      default: false,
    },
    totalItems: {
      type: Number,
      required: false,
    },
    pagination: {
      type: Object,
      required: true,
    },
  },
  computed: {
    headers() {
      return [
        {
          text: this.$t('common.name'),
          value: 'name',
        },
        {
          text: this.$t('common.fullscreen'),
          value: 'fullscreen',
          sortable: false,
        },
        {
          text: this.$t('common.enabled'),
          value: 'enabled',
        },
        {
          text: this.$t('common.interval'),
          value: 'interval',
        },
        {
          text: this.$t('common.actionsLabel'),
          value: 'actions',
          sortable: false,
        },
      ];
    },
  },
  methods: {
    getPlaylistRouteById(id, userAction = false) {
      return {
        name: 'playlist',
        params: { id, userAction },
        query: { autoplay: true },
      };
    },

    getPlaylistRouteFullUrlById(id) {
      const { href } = this.$router.resolve(this.getPlaylistRouteById(id));

      return removeTrailingSlashes(`${APP_HOST}${href}`);
    },

    onSuccessCopied() {
      this.$popups.success({ text: this.$t('success.linkCopied') });
    },

    onErrorCopied() {
      this.$popups.success({ text: this.$t('errors.default') });
    },
  },
};
</script>
