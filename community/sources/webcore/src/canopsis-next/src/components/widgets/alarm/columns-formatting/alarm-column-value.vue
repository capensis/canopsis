<template lang="pug">
  v-runtime-template(v-if="column.template", :template="columnContent")
  color-indicator-wrapper(
    v-else,
    :type="column.colorIndicator",
    :entity="alarm.entity",
    :alarm="alarm"
  )
    alarm-column-cell(
      :alarm="alarm",
      :widget="widget",
      :column="column",
      :columns-filters="columnsFilters"
    )
</template>

<script>
import { get } from 'lodash';
import VRuntimeTemplate from 'v-runtime-template';

import { compile } from '@/helpers/handlebars';

import ColorIndicatorWrapper from '@/components/common/table/color-indicator-wrapper.vue';

import AlarmColumnCell from './alarm-column-cell.vue';

export default {
  components: {
    ColorIndicatorWrapper,
    VRuntimeTemplate,
    AlarmColumnCell,
  },
  props: {
    alarm: {
      type: Object,
      required: true,
    },
    widget: {
      type: Object,
      required: true,
    },
    column: {
      type: Object,
      required: true,
    },
    columnsFilters: {
      type: Array,
      default: () => [],
    },
  },
  asyncComputed: {
    columnContent: {
      lazy: true,

      async get() {
        const context = {
          value: get(this.alarm, this.column.value, ''),
          alarm: this.alarm,
          entity: this.alarm.entity,
        };
        const compiledTemplate = await compile(this.column.template, context);

        return `<div>${compiledTemplate}</div>`;
      },
      default: '',
    },
  },
};
</script>
