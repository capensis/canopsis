<template lang="pug">
  v-text-field(
    v-validate,
    v-field="value",
    :label="label || $t('common.id')",
    :error-messages="errors.collect(name)",
    :disabled="disabled",
    :readonly="disabled",
    :name="name",
    @input="errors.remove(name)"
  )
    c-help-icon(
      v-if="helpText",
      slot="append",
      :text="helpText",
      icon="help",
      color="grey darken-1",
      left
    )
</template>

<script>
export default {
  inject: ['$validator'],
  model: {
    prop: 'value',
    event: 'input',
  },
  props: {
    value: {
      type: String,
      required: true,
    },
    label: {
      type: String,
      default: '',
    },
    name: {
      type: String,
      default: '_id',
    },
    helpText: {
      type: String,
      required: false,
    },
    disabled: {
      type: Boolean,
      default: false,
    },
  },
};
</script>
