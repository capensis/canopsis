<template>
  <c-select-field
    class="c-lazy-search-field mt-4"
    v-validate="rules"
    v-field="value"
    :search-input="search"
    :label="label"
    :loading="loading"
    :items="items"
    :name="name"
    :item-text="getItemText"
    :item-value="itemValue"
    :item-disabled="itemDisabled"
    :multiple="isMultiply"
    :deletable-chips="isMultiply"
    :small-chips="isMultiply"
    :error-messages="errors.collect(name)"
    :disabled="disabled"
    :menu-props="{ contentClass: 'c-lazy-search-field__list', eager: true }"
    :clearable="clearable"
    :autocomplete="autocomplete"
    :combobox="!autocomplete"
    :return-object="returnObject"
    :no-data-text="noDataText"
    no-filter
    dense
    @focus="onFocus"
    @blur="onBlur"
    @update:search-input="debouncedUpdateSearch"
  >
    <template #item="{ item, attrs, on, parent }">
      <slot
        name="item"
        :attrs="attrs"
        :on="on"
        :item="item"
        :parent="parent"
      >
        <v-list-item
          class="c-lazy-search-field--tile"
          v-bind="attrs"
          v-on="on"
        >
          <slot
            name="icon"
            :item="item"
          />
          <v-list-item-content>
            {{ getItemText(item) }}
          </v-list-item-content>
          <span class="ml-4 grey--text">{{ item.type }}</span>
        </v-list-item>
      </slot>
    </template>
    <template #append-item="">
      <div
        class="c-lazy-search-field__append"
        ref="append"
      />
    </template>
    <template #selection="{ item, index }">
      <slot
        name="selection"
        :item="item"
        :index="index"
      >
        <v-chip
          class="c-lazy-search-field__chip"
          v-if="isMultiply"
          small
          close
          @input="removeItemFromArray(index)"
        >
          <span class="text-truncate">{{ getItemText(item) }}</span>
        </v-chip>
        <slot
          v-else
          name="selection"
          :item="item"
        >
          {{ getItemText(item) }}
        </slot>
      </slot>
    </template>
  </c-select-field>
</template>

<script>
import {
  debounce,
  isArray,
  isString,
  isFunction,
  get,
} from 'lodash';

import { formArrayMixin } from '@/mixins/form';

export default {
  inject: ['$validator'],
  mixins: [formArrayMixin],
  model: {
    prop: 'value',
    event: 'input',
  },
  props: {
    value: {
      type: [Array, String, Object],
      default: '',
    },
    name: {
      type: String,
      default: 'entities',
    },
    label: {
      type: String,
      required: false,
    },
    itemText: {
      type: [String, Function],
      default: '_id',
    },
    itemValue: {
      type: String,
      default: '_id',
    },
    noDataText: {
      type: String,
      required: false,
    },
    disabled: {
      type: Boolean,
      default: false,
    },
    returnObject: {
      type: Boolean,
      default: false,
    },
    autocomplete: {
      type: Boolean,
      default: false,
    },
    clearable: {
      type: Boolean,
      default: false,
    },
    itemDisabled: {
      type: [String, Array, Function],
      required: false,
    },
    required: {
      type: Boolean,
      default: false,
    },
    loading: {
      type: Boolean,
      default: false,
    },
    hasMore: {
      type: Boolean,
      default: false,
    },
    items: {
      type: Array,
      default: () => [],
    },
    search: {
      type: String,
      default: null,
    },
  },
  data() {
    return {
      isFocused: false,
    };
  },
  computed: {
    rules() {
      return {
        required: this.required,
      };
    },

    isMultiply() {
      return isArray(this.value);
    },
  },
  created() {
    this.debouncedUpdateSearch = debounce(this.updateSearch, 300);
  },
  mounted() {
    this.observer = new IntersectionObserver(this.intersectionHandler);

    this.observer.observe(this.$refs.append);
  },
  beforeDestroy() {
    this.observer.unobserve(this.$refs.append);
  },
  methods: {
    getItemText(item) {
      if (isString(item)) {
        return item;
      }

      return isFunction(this.itemText) ? this.itemText(item) : get(item, this.itemText);
    },

    intersectionHandler(entries) {
      const [entry] = entries;

      if (entry.isIntersecting && this.hasMore) {
        this.fetchItems();
      }
    },

    updateSearch(value) {
      if (this.isFocused) {
        this.$emit('update:search', value);
      }
    },

    onFocus() {
      this.isFocused = true;

      if (!this.items.length) {
        this.$emit('fetch');
      }
    },

    onBlur() {
      this.isFocused = false;
    },

    fetchItems() {
      this.$emit('fetch:more');
    },

    updateValue(value) {
      this.updateModel(this.returnObject ? value : get(value, this.itemValue));
    },
  },
};
</script>

