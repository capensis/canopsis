<template lang="pug">
  v-tooltip(v-if="value", :max-width="maxWidth", :top="top", :right="right", :bottom="bottom", :left="left")
    v-icon(slot="activator", :color="color", :size="size") sync_problem
    span {{ message }}
</template>

<script>
import { getDiffBetweenDate } from '@/helpers/date/date';
import { convertDurationToString } from '@/helpers/date/duration';

export default {
  props: {
    value: {
      type: Number,
      default: 0,
    },
    color: {
      type: String,
      default: 'error',
    },
    maxWidth: {
      type: [String, Number],
      required: false,
    },
    top: {
      type: Boolean,
      required: false,
    },
    right: {
      type: Boolean,
      required: false,
    },
    bottom: {
      type: Boolean,
      required: false,
    },
    left: {
      type: Boolean,
      required: false,
    },
    size: {
      type: String,
      required: false,
    },
  },
  computed: {
    durationDiff() {
      return getDiffBetweenDate(Date.now(), this.value);
    },

    message() {
      return this.$t('icons.noEvents', {
        duration: convertDurationToString(this.durationDiff),
      });
    },
  },
};
</script>
