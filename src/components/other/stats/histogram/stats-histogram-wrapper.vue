<template lang="pug">
  v-container(fluid)
    v-btn(icon, @click="showSettings")
      v-icon settings
    stats-histogram(:labels="labels", :datasets="datasets")
</template>

<script>
import omit from 'lodash/omit';
import entitiesStatsMixin from '@/mixins/entities/stats';
import sideBarMixin from '@/mixins/side-bar/side-bar';
import widgetQueryMixin from '@/mixins/widget/query';
import entitiesUserPreferenceMixin from '@/mixins/entities/user-preference';
import { SIDE_BARS } from '@/constants';
import StatsHistogram from './stats-histogram.vue';

export default {
  components: {
    StatsHistogram,
  },
  mixins: [entitiesStatsMixin, sideBarMixin, widgetQueryMixin, entitiesUserPreferenceMixin],
  props: {
    widget: {
      type: Object,
      required: true,
    },
    rowId: {
      type: String,
      required: true,
    },
  },
  data() {
    return {
      stats: {},
    };
  },
  computed: {
    labels() {
      return Object.keys(this.stats);
    },
    datasets() {
      const datasets = [];
      Object.keys(this.widget.parameters.stats).map((stat) => {
        const data = [];
        Object.values(this.stats).map((group) => {
          if (group.aggregations) {
            data.push(group.aggregations[stat].sum);
          }
          return data;
        });
        return datasets.push({ label: stat, data, backgroundColor: this.widget.parameters.statsColors[stat] });
      });
      return datasets;
    },
  },
  methods: {
    showSettings() {
      this.showSideBar({
        name: SIDE_BARS.statsHistogramSettings,
        config: {
          widget: this.widget,
          rowId: this.rowId,
        },
      });
    },
    fetchList() {
      this.widget.parameters.groups.map(async (group) => {
        const stat = await this.fetchStats({ params: { ...omit(this.widget.parameters, ['groups', 'statsColors']), mfilter: group.filter || {} }, aggregate: ['sum'] });
        this.$set(this.stats, group.title, stat);
      });
    },
  },
};
</script>

