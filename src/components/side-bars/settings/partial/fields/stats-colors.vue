<template lang="pug">
  v-list-group
    v-list-tile(slot="activator") Stats colors
    v-container(fluid)
      v-layout(v-for="(stat, key) in stats", align-center, :key="key")
        v-flex
          div {{ key }}:
        v-flex
          v-btn(@click="e => showColorPickerModal(key)") Pick color
        v-flex
          div.py-2.text-xs-center(:style="{ backgroundColor: value[key] }") {{ value[key] }}
</template>

<script>
import set from 'lodash/set';
import modalMixin from '@/mixins/modal/modal';
import { MODALS } from '@/constants';

export default {
  mixins: [modalMixin],
  props: {
    value: {
      type: Object,
      default() {
        return {};
      },
    },
    stats: {
      type: Object,
    },
  },
  methods: {
    showColorPickerModal(key) {
      const newVal = { ...this.value };
      this.showModal({
        name: MODALS.colorPicker,
        config: {
          title: 'modals.colorPicker.title',
          action: color => this.$emit('input', set(newVal, key, color)),
        },
      });
    },
  },
};
</script>

